/**
 * CHECK: Type Safety
 * Flags unsafe type patterns that bypass the type system or suppress linting.
 *
 */

import fs from "node:fs";
import path from "node:path";
import { c, ROOT, relPath, walkDir } from "./utils.js";

export function run() {
  const DIRS = [
    path.join(ROOT, "src"),
    path.join(ROOT, "convex"),
    path.join(ROOT, "scripts"),
    path.join(ROOT, "e2e"),
    path.join(ROOT, "emails"),
  ];

  const SKIP_PATTERNS = [
    /routeTree\.gen\.ts$/, // Auto-generated by TanStack Router
    /\.stories\.tsx?$/, // Storybook files
    /\.d\.ts$/, // Type declaration files
    /check-type-safety\.js$/, // This file (contains pattern strings)
    /convex\/_generated\//, // Auto-generated Convex types
  ];

  // Files where only suppression patterns are checked (type assertions are expected)
  const TEST_FILE_PATTERNS = [/\.test\.tsx?$/, /\.spec\.tsx?$/, /e2e\/fixtures\.ts$/];

  // Type assertions that bypass the type system (skip in test/spec files)
  const TYPE_PATTERNS = [
    { regex: /\)\s*as\s+any\b/, message: "Unsafe 'as any' type assertion" },
    { regex: /\)\s*as\s+unknown\s+as\b/, message: "Unsafe 'as unknown as' double assertion" },
    { regex: /\)\s*as\s+never\b/, message: "Unsafe 'as never' type assertion" },
    { regex: /:\s*any\b(?!\s*\))/, message: "Explicit 'any' type annotation" },
    { regex: /<any>/, message: "Generic 'any' type parameter" },
    { regex: /Record<string,\s*any>/, message: "Record with 'any' value type" },
  ];

  // Lint suppression patterns (checked in ALL files including tests)
  const SUPPRESSION_PATTERNS = [
    { regex: /@ts-ignore\b/, message: "@ts-ignore suppresses type errors" },
    { regex: /@ts-nocheck\b/, message: "@ts-nocheck disables type checking" },
    { regex: /@ts-expect-error\b/, message: "@ts-expect-error suppresses type errors" },
    { regex: /biome-ignore\b/, message: "biome-ignore suppresses lint rules" },
    { regex: /eslint-disable\b/, message: "eslint-disable suppresses lint rules" },
  ];

  let errorCount = 0;
  const errors = [];

  function checkFile(filePath) {
    const rel = relPath(filePath);
    if (SKIP_PATTERNS.some((p) => p.test(rel))) return;

    const isTestFile = TEST_FILE_PATTERNS.some((p) => p.test(rel));
    const patterns = isTestFile
      ? SUPPRESSION_PATTERNS
      : [...TYPE_PATTERNS, ...SUPPRESSION_PATTERNS];

    const content = fs.readFileSync(filePath, "utf-8");
    const lines = content.split("\n");

    lines.forEach((line, index) => {
      for (const { regex, message } of patterns) {
        if (regex.test(line)) {
          errors.push(`  ${c.red}ERROR${c.reset} ${rel}:${index + 1} - ${message}`);
          errorCount++;
        }
      }
    });
  }

  for (const dir of DIRS) {
    if (!fs.existsSync(dir)) continue;
    const files = walkDir(dir, { extensions: new Set([".ts", ".tsx", ".js", ".mjs"]) });
    for (const f of files) checkFile(f);
  }

  return {
    passed: errorCount === 0,
    errors: errorCount,
    detail: errorCount > 0 ? `${errorCount} error(s)` : null,
    messages: errors,
  };
}
