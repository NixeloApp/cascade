/**
 * CHECK: Type Safety
 * Flags unsafe type patterns that bypass the type system or suppress linting.
 *
 * @strictness STRICT - Blocks CI. ~40 files in allowlist for unavoidable patterns.
 */

import fs from "node:fs";
import path from "node:path";
import { c, ROOT, relPath, walkDir } from "./utils.js";

export function run() {
  const DIRS = [path.join(ROOT, "src"), path.join(ROOT, "convex"), path.join(ROOT, "scripts")];
  const IGNORE_PATTERNS = [
    /routeTree\.gen\.ts$/, // Auto-generated by TanStack Router
    /\.stories\.tsx?$/, // Storybook files
    /check-type-safety\.js$/, // This file (contains pattern strings)
    // Legacy test files with violations - fix incrementally, do NOT add new files
    /ImportExportModal\.test\.tsx$/, // 14 violations - TODO: use proper mocks
    /IssueCard\.test\.tsx$/, // 3 violations - TODO: fix type assertions
    /hourCompliance\.test\.ts$/, // 9 violations - TODO: fix biome-ignore
    /githubOAuth\.test\.ts$/, // 1 violation - TODO: fix biome-ignore
    /googleOAuth\.test\.ts$/, // 1 violation - TODO: fix biome-ignore
    /meetingBotPending\.test\.ts$/, // 1 violation - TODO: fix biome-ignore
    /meetingBotTimeout\.test\.ts$/, // 1 violation - TODO: fix as any
    /otpErrorHandling\.test\.ts$/, // 4 violations - TODO: fix ts-expect-error
    /otpVerificationTestEnv\.test\.ts$/, // 6 violations - TODO: fix suppressions
    /purge\.test\.ts$/, // 2 violations - TODO: fix suppressions
    /usersPerfFastPath\.test\.ts$/, // 4 violations - TODO: fix biome-ignore
    /usersPerfShared\.test\.ts$/, // 2 violations - TODO: fix biome-ignore
    /webhooksCrash\.test\.ts$/, // 3 violations - TODO: fix biome-ignore
    /useIssueModal\.ts$/, // TanStack Router generic hook requires type casting
    /convex\/lib\//, // Convex internal utilities with complex generics
    /convex\/softDeleteCleanup\.ts$/, // Convex internal cleanup utility
    /convex\/issues\/queries\.ts$/, // Convex search index type mismatch requires casting
    /convex\/calendarEvents\.ts$/, // Multikey index query requires explicit casting
    /src\/components\/KeyboardShortcutsHelp\.tsx$/, // Contains explicit suppressions
    /convex\/automationRules\.ts$/, // Complexity threshold update
    /convex\/otpPasswordReset\.ts$/, // Complexity threshold update
    /convex\/otpVerification\.ts$/, // Complexity threshold update
    /convex\/rateLimits\.ts$/, // Type assertion required
    /src\/components\/App\/AppSidebar\.tsx$/, // Complexity: multiple nav sections - TODO: split into NavSection components
    /src\/components\/IssueDetail\/CreateIssueModal\.tsx$/, // Complexity: form + validation + AI - TODO: extract form sections
    /src\/components\/Documents\/DocumentHeader\.tsx$/, // Complexity: conditional actions - TODO: extract DocumentActions
    /src\/components\/IssueDetail\/IssueCard\.tsx$/, // Complexity: drag-drop + selection - TODO: split DraggableIssueCard
    /src\/components\/IssuesCalendarView\.tsx$/, // a11y: calendar cells are drop targets (clickable divs)
    /src\/components\/PlateEditor\.tsx$/, // Complexity: editor operations - TODO: extract DocumentControls
    /src\/components\/RoadmapView\.tsx$/, // Complexity: timeline + SVG - TODO: extract TimelineRenderer
    /src\/components\/Settings\/AvatarUploadModal\.tsx$/, // a11y: drop zone requires div for keyboard handling
    /src\/components\/Settings\/CoverImageUploadModal\.tsx$/, // a11y: drop zone requires div for keyboard handling
    /convex\/users\.ts$/, // Complexity: search with filters - TODO: extract searchUsersHelper
    /src\/components\/Plate\/MentionInputElement\.tsx$/, // Intentional useEffect deps pattern for combobox reset
    /src\/components\/IssueDetail\/IssueMetadataSection\.tsx$/, // Complexity: conditional inline editing for each field
  ];

  let errorCount = 0;
  const errors = [];

  const PATTERNS = [
    { regex: /\)\s*as\s+any\b/, message: "Unsafe 'as any' type assertion" },
    { regex: /\)\s*as\s+unknown\s+as\b/, message: "Unsafe 'as unknown as' double assertion" },
    { regex: /\)\s*as\s+never\b/, message: "Unsafe 'as never' type assertion" },
    { regex: /@ts-ignore\b/, message: "@ts-ignore suppresses type errors" },
    { regex: /@ts-nocheck\b/, message: "@ts-nocheck disables type checking" },
    { regex: /@ts-expect-error\b/, message: "@ts-expect-error suppresses type errors" },
    { regex: /biome-ignore\b/, message: "biome-ignore suppresses lint rules" },
  ];

  function checkFile(filePath) {
    const rel = relPath(filePath);
    if (IGNORE_PATTERNS.some((p) => p.test(rel))) return;

    const content = fs.readFileSync(filePath, "utf-8");
    const lines = content.split("\n");

    lines.forEach((line, index) => {
      for (const { regex, message } of PATTERNS) {
        if (regex.test(line)) {
          errors.push(`  ${c.red}ERROR${c.reset} ${rel}:${index + 1} - ${message}`);
          errorCount++;
        }
      }
    });
  }

  for (const dir of DIRS) {
    if (!fs.existsSync(dir)) continue;
    const files = walkDir(dir, { extensions: new Set([".ts", ".tsx", ".js", ".mjs"]) });
    for (const f of files) checkFile(f);
  }

  return {
    passed: errorCount === 0,
    errors: errorCount,
    detail: errorCount > 0 ? `${errorCount} error(s)` : null,
    messages: errors,
  };
}
